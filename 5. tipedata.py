# 1. Tipe data INTEGER 
# Tipe data int adalah angka bulat baik itu positif maupun negatif sebesar apapun, asal tidak mengandung desimal
a = 2
b = 7823652739871965
c = -327482390
print (a)
print (b)
print (c)

# 2. Tipe data FLOAT
# Tipe data float adalah angka atau desimal baik positif maupun negatif
d = 2.90
e = 1.487932589
f = -11.59
g = 1e-2 

# 3. Tipe data STRING
